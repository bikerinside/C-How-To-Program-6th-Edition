/*
4.12

(Prime Numbers) Write a program to calculate and print a list of all prime numbers from
1 to 100.

*/

#include <stdio.h>

int main()

{

  for(int i = 1; i <= 100; i += 2) printf("\n%d", i);

  return 0;

}

