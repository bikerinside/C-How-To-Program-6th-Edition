/*

4.11 

(Calculating the Sum of Multiples) Write a program to calculate and print the sum of all
multiples of 7 from 1 to 100.

*/

#include <stdio.h>

int main()

{

  for(int i = 1; i <= 100; i++ ) 
    printf("\nn = %d\t%d", i, i*7);
  
  return 0;

}


